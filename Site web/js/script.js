// Fonction pour afficher le bouton quand on descend la page
window.onscroll = function() {scrollFunction()};

function scrollFunction() {
  if (document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) {
    document.getElementById("myBtn").style.display = "block";
  } else {
    document.getElementById("myBtn").style.display = "none";
  }
}

// Fonction pour remonter en haut de la page quand on clique sur le bouton
function topFunction() {
  document.body.scrollTop = 0; // Pour les anciens navigateurs
  document.documentElement.scrollTop = 0; // Pour les navigateurs modernes
}
